-- 3. Atomic claim RPC for the worker
-- Picks the oldest pending job, marks it processing, returns the row.
-- FOR UPDATE SKIP LOCKED makes this safe with multiple parallel workers.
CREATE OR REPLACE FUNCTION public.claim_next_export_job(p_worker_id TEXT)
RETURNS SETOF public.analysis_export_jobs
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN QUERY
  UPDATE public.analysis_export_jobs
  SET
    status = 'processing',
    started_at = now(),
    worker_id = p_worker_id,
    attempts = attempts + 1
  WHERE id = (
    SELECT id
    FROM public.analysis_export_jobs
    WHERE status = 'pending'
    ORDER BY created_at ASC
    FOR UPDATE SKIP LOCKED
    LIMIT 1
  )
  RETURNING *;
END;
$$;

-- Only the service_role (worker) should call this.
REVOKE EXECUTE ON FUNCTION public.claim_next_export_job(TEXT) FROM PUBLIC;
REVOKE EXECUTE ON FUNCTION public.claim_next_export_job(TEXT) FROM anon, authenticated;
GRANT EXECUTE ON FUNCTION public.claim_next_export_job(TEXT) TO service_role;